function soustraction(top,bot) {
    return top-bot;
}

function division(com,pote) {
    return com/pote;
}

function multiplication(omel,ette) {
    return omel*ette;
}

function pourcentage(confi,ture){
    return (confi/100)*ture;
}

function kilometre(km,h){
  return km/h+" km/h"; //METERS NOT KILOMETERS MINUTES NOT HOURS
}

